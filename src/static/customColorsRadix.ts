export function getColorScale(colors: { [key: string]: { [key: string]: string } }) {
  const colorScale: { [key: string]: string } = {};
  for (const colorSetKey in colors) {
    if(colors.hasOwnProperty(colorSetKey)){
      const colorSet = colors[colorSetKey];
          for (const key in colorSet) {
             if(colorSet.hasOwnProperty(key)){
                colorScale[colorSetKey + key.replace('A', '')] = colorSet[key];
             }
            
          }
    }
    
  }
  return colorScale;
}

